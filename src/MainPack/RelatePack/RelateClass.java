package MainPack.RelatePack;

public class RelateClass {
      public int greater(int a,int b) {
    	  if(a>b) {
    		  return a;
    	  } 
    	      return b;
      }
      public int smaller(int a,int b) {
    	  if(a<b) {
    		  return a;
    	  }
    		  return b;
      }
      public boolean equal(int a,int b) {
    	  if(a==b) {
    		  return true;
    	  }
		return false;
      }
}
